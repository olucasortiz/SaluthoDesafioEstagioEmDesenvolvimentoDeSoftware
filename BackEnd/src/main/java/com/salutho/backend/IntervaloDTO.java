package com.salutho.backend;

public class IntervaloDTO {
    private int inicio;
    private int fim;

    public IntervaloDTO() {}

    public int getInicio() {
        return inicio;
    }

    public void setInicio(int inicio) {
        this.inicio = inicio;
    }

    public int getFim() {
        return fim;
    }

    public void setFim(int fim) {
        this.fim = fim;
    }
}
